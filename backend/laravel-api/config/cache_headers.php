<?php

declare(strict_types=1);

return [
    /*
    |--------------------------------------------------------------------------
    | Cache Headers Enabled
    |--------------------------------------------------------------------------
    |
    | キャッシュヘッダー機能の有効/無効を制御します。
    | 開発環境では無効化、本番環境では有効化することを推奨します。
    |
    */
    'enabled' => env('CACHE_HEADERS_ENABLED', true),

    /*
    |--------------------------------------------------------------------------
    | Default TTL (Time To Live)
    |--------------------------------------------------------------------------
    |
    | デフォルトのキャッシュTTL（秒）を設定します。
    | エンドポイント固有の設定がない場合にこの値が使用されます。
    |
    */
    'default_ttl' => env('CACHE_HEADERS_DEFAULT_TTL', 300), // 5 minutes

    /*
    |--------------------------------------------------------------------------
    | TTL by Path
    |--------------------------------------------------------------------------
    |
    | エンドポイントごとのキャッシュTTL（秒）を設定します。
    | パスは request()->path() の戻り値と一致させる必要があります。
    | 例: 'api/health' => 60
    |
    */
    'ttl_by_path' => [
        'api/health' => 60,        // ヘルスチェック: 1分
        'api/user' => 300,         // ユーザー情報: 5分
        'api/posts' => 600,        // 投稿一覧: 10分
    ],
];
